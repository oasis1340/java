package apiTest;

/**
 * @author Jang
 * Calculator for divide
 */

public class Divide {
	
	/**
	 * 
	 * @param num1 : first number
	 * @param num2 : last number
	 * @return : result <br />
	 * example) divide(10, 3) : 3
	 * @throws ArithmeticException
	 * @see {@link Math}
	 */
	
	public int divide(int num1, int num2) {
		return num1 / num2;
	}
}
